class Solution
{
    public:
    //Function to find the maximum money the thief can get.
    int FindMaxSum(int arr[], int n)
    {
        int r1=arr[0],r2=0;
        for(int i=2; i<=n; i++){
            int t = max(r2+arr[i-1],r1);
            r2 = r1;
            r1 = t;
        }
        return r1;
    }
};

